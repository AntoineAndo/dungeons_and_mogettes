const imageToAscii = require("image-to-ascii");

exports.getAsciiFromImage = getAsciiFromImage = function(url, callback){
	imageToAscii(url, {
		colored: false,
		pxWidth: 1,
		size: {
			height: 100
		},
		size_options: {
			preserve_aspect_ratio: true,
			fit_screen: true
		},
		reverse: false
	}, (err, converted) => {
		callback(err || converted);
	});
};
